print(suffix)  # programming.html#how-can-i-pass-optional-or-keyword-parameters-from-one-function-to-another
"""

# ‚óè –†–∞—Å–ø–∞–∫–æ–≤–∫–∞ —Å–æ –∑–≤—ë–∑–¥–æ—á–∫–æ–π
# –ï—â—ë –æ–¥–∏–Ω —Å–ø–æ—Å–æ–± –ø—Ä–∏–º–µ–Ω–µ–Ω–∏—è –∑–≤—ë–∑–¥–æ—á–∫–∏ ‚Äî —Ä–∞—Å–ø–∞–∫–æ–≤–∫–∞ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –∫–æ–ª–ª–µ–∫—Ü–∏–∏.
# –î–ª–∏–Ω–Ω—ã–π –≤–∞—Ä–∏–∞–Ω—Ç –≤—ã–≤–æ–¥–∞ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏ –≤ –æ–¥–Ω—É —Å—Ç—Ä–æ–∫—É —Å —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª–µ–º —Ç–∞–±—É–ª—è—Ü–∏–µ–π:
"""
data = [2, 4, 6, 8, 10, ]
